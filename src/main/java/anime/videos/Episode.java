package anime.videos; 
public class Episode{
    private String title;
    private String episode;
    private String url;
    private String image_url;

    public String getTitle() {
        return title;
    }

    public String getEpisode() {
        return episode;
    }

    public String getUrl() {
        return url;
    }

    public String getImage_url() {
        return image_url;
    }
}
